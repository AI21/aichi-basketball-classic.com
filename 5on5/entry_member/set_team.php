function preFilters($str)
{
	if (ini_get('magic_quotes_gpc')){
		$str = stripslashes_deep(nl2br(htmlentities($str, ENT_QUOTES,'UTF-8')));
	}else{
		$str = nl2br(htmlentities($str, ENT_QUOTES,'UTF-8'));
	}
	return $str;
}
